package main

import "strings"

type fontData struct {
	height int
	digits map[rune][]string
}

var fonts = map[string]*fontData{
	"block": &blockFont,
	"slim":  &slimFont,
	"dot":   &dotFont,
}

func renderDigit(f *fontData, ch rune) string {
	rows, ok := f.digits[ch]
	if !ok {
		return string(ch)
	}
	return strings.Join(rows, "\n")
}

func fontSpacer(height int) string {
	rows := make([]string, height)
	for i := range rows {
		rows[i] = " "
	}
	return strings.Join(rows, "\n")
}

