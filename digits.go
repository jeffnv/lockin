func bigDigit(ch rune) string {
	rows, ok := bigDigits[ch]
	if !ok {
		return string(ch)
	}
	return strings.Join(rows[:], "\n")
}

func digitSpacer() string {
	var rows [7]string
	for i := range rows {
		rows[i] = " "
	}
	return strings.Join(rows[:], "\n")
}
